// Helper to generate available dates
const generateDates = (_basePrice: number): ProviderTrip['availableDates'] => {
  const dates = [];
  const today = new Date();

  for (let i = 1; i <= 4; i++) {
    const startDate = new Date(today);
    startDate.setDate(today.getDate() + (i * 15) + 7);
    const endDate = new Date(startDate);
    endDate.setDate(startDate.getDate() + 6);

    dates.push({
      id: `date-${i}`,
      startDate: startDate.toISOString().split('T')[0],
      endDate: endDate.toISOString().split('T')[0],
      spotsLeft: Math.floor(Math.random() * 8) + 2,
      priceModifier: i === 2 ? 1.2 : 1.0, // Peak season for second date
    });
  }

  return dates;
};

