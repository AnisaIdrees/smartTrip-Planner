// Helper function to get trips by category
export const getTripsByCategory = (category: TripCategory | 'all'): ProviderTrip[] => {
  if (category === 'all') return providerTrips;
  return providerTrips.filter((trip) => trip.category === category);
};

// Helper function to get trip by ID
export const getTripById = (id: string): ProviderTrip | undefined => {
  return providerTrips.find((trip) => trip.id === id);
};

// Helper function to get category info
export const getCategoryInfo = (category: TripCategory): CategoryInfo | undefined => {
  return categories.find((cat) => cat.id === category);
};
